# ---------------------------------------------------------------------------
# Arabic QWERTY Keyboard Layout
# ---------------------------------------------------------------------------
# Standard Arabic keyboard layout rows for proximity-based penalty calculation.
# Used by the Levenshtein fuzzy matcher to reduce penalty for adjacent-key typos.

KEYBOARD_ROWS: list[str] = [
    "ضصثقفغعهخحجد",      # Row 1 (top)
    "شسيبلاتنمكط",        # Row 2 (home)
    "ئءؤرلاىةوز",          # Row 3 (bottom)
]

# Build a flat adjacency map: each character -> set of immediate neighbors
KEYBOARD_NEIGHBORS: dict[str, frozenset[str]] = {}
for row in KEYBOARD_ROWS:
    for i, ch in enumerate(row):
        neighbors: set[str] = set()
        if i > 0:
            neighbors.add(row[i - 1])
        if i < len(row) - 1:
            neighbors.add(row[i + 1])
        KEYBOARD_NEIGHBORS[ch] = frozenset(neighbors)
